Weeknumber=input("Enter Weeknumber from 1 to 7 : ")
if int(Weeknumber)==1:
    print ("Its Monday")
elif int(Weeknumber)==2:
    print ("Its Tuesday")
elif int(Weeknumber)==3:
    print ("Its Wednesday")
elif int(Weeknumber)==4:
    print ("Its Thursday")
elif int(Weeknumber)==5:
    print ("Its Friday")
elif int(Weeknumber)==6:
    print ("Its Saturday")
elif int(Weeknumber)==7:
    print ("Its Sunday")
else:
    print("Invalid week number")